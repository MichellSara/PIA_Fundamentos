<Title> Adopcion </Title>
<h1><center>REQUISITOS</center></h1>
<h1><font face="Arial">Ser mayor de edad: Se presentara el DNI de identificacion.</h1>
<h1>Comprobante de domicilio: Documento que demuestre que la persona es propietaria del domicilio donde vivira la mascota o, en caso de alquiler, que el arrendatario permite residir con animales.</h1>
<h1>Contrato de adopcion. Debes firmar un acta que te compromete a:
Cuidar de tu mascota y mantenerla en unas condiciones optimas de espacio, tiempo, alimentacion, ejercicio...
Dotarle de los cuidados veterinarios que necesite. La mascota se entregara desparasitada y con las correspondientes vacunas, los perros y llevaran, ademas, microchip.</h1>
<h1>Notificar cualquier cambio a la asociacion (perdida, muerte...).</h1>
